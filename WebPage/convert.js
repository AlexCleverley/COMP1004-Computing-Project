var arrayfromsearch = records
$('#search-input').on('keyup', function(){
	var value = $(this).val()
	var array2 = searchTable(value, records)
	arrayfromsearch = array2
	buildTable(array2)
})

$('th').on('click', function(){
	var column = $(this).data('column')
	var order = $(this).data('order')
	
	if(order == 'desc'){
		$(this).data('order', "asc")
		arrayfromsearch = arrayfromsearch.sort((a,b) => a[column] > b[column] ? 1:
						 -1)
	}else{
		$(this).data('order', "desc")
		arrayfromsearch = arrayfromsearch.sort((a,b) => a[column] < b[column] ? 1:
						 -1)
	}
	buildTable(arrayfromsearch)
})


function searchTable(value, data){
	var fdata=[]
	value = value.toLowerCase()
	
	for(var i = 0; i < data.length; i++) {
		var name = data[i].ProtocolName.toLowerCase()
		if (name.includes(value)){
			fdata.push(data[i])
		}
	}
	
	return fdata
}




buildTable(records)
function buildTable(data){
	var table = document.getElementById('myTable')
	
	table.innerHTML = ''
	for (var i = 0; i < data.length; i++) {
		var row = ` <tr> 
						<td style="text-align:center">${data[i]["Source.IP"]}</td>
						<td style="text-align:center">${data[i]["Destination.IP"]}</td>
						<td style="text-align:center">${data[i].Timestamp}</td>
						<td style="text-align:center">${data[i]["Total.Fwd.Packets"]}</td>
						<td style="text-align:center">${data[i]["Total.Backward.Packets"]}</td>
						<td style="text-align:center">${data[i].L7Protocol}</td>
						<td style="text-align:center">${data[i].ProtocolName}</td>
					</tr>`
		table.innerHTML += row
	}
}
